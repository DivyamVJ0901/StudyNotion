const mongoose = require('mongoose');

// Define Contact Schema
const contactSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  phone: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  }
});

// Create and export Contact model
const Contact = mongoose.model('Contact', contactSchema);
module.exports = Contact;
